fofr=0:0.001:1;
arg=2.*pi.*fofr;
nume=2.*(1.-cos(arg));
den11=(1+0.5*0.5);
den12=(2*0.5).*cos(arg);
den1=den11-den12;
den21=1+0.9*0.9;
den22=(2.*0.9).*cos(arg);
den2=den21-den22;
den31=(1+0.98*0.98);
den32=((2.*0.98).*cos(arg));
den3=den31-den32;
resp1=nume./den1;
%max1=max(resp1);
%resp1=resp1./max1;
resp2=nume./den2;
%max2=max(resp2);
%resp2=resp2./max2;
resp3=nume./den3;
%max3=max(resp3);
%resp3=resp3./max3;
plot(fofr,resp1,fofr,resp2,'-.',fofr,resp3,'--');
